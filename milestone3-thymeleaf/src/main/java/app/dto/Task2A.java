package app.dto;

public class Task2A {
    private String country;
    private String changeInLoss;
    private String commodity;
    private String activity;
    private String foodSupplyStage;
    private String year;
    private String causeOfLoss;
    private double lossValue;

    // Getters and Setters
    public String getCountry() {
        return country;
    }

    public void setCountry(String country) {
        this.country = country;
    }

    public String getChangeInLoss() {
        return changeInLoss;
    }

    public void setChangeInLoss(String changeInLoss) {
        this.changeInLoss = changeInLoss;
    }

    public String getCommodity() {
        return commodity;
    }

    public void setCommodity(String commodity) {
        this.commodity = commodity;
    }

    public String getActivity() {
        return activity;
    }

    public void setActivity(String activity) {
        this.activity = activity;
    }

    public String getFoodSupplyStage() {
        return foodSupplyStage;
    }

    public void setFoodSupplyStage(String foodSupplyStage) {
        this.foodSupplyStage = foodSupplyStage;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public String getCauseOfLoss() {
        return causeOfLoss;
    }

    public void setCauseOfLoss(String causeOfLoss) {
        this.causeOfLoss = causeOfLoss;
    }

    public double getLossValue() {
        return lossValue;
    }

    public void setLossValue(double lossValue) {
        this.lossValue = lossValue;
    }
}
